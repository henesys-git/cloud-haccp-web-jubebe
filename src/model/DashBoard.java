package model;

public class DashBoard {
	private String sensorName;
	private String curTestTime;
	private String nextTestTime;
	private String sensorId;
	private String detectCount;
	private String detectCountHour;
	private String detectTime;
	
	public String getSensorName() {
		return sensorName;
	}
	public void setSensorName(String sensorName) {
		this.sensorName = sensorName;
	}
	public String getCurTestTime() {
		return curTestTime;
	}
	public void setCurTestTime(String curTestTime) {
		this.curTestTime = curTestTime;
	}
	public String getNextTestTime() {
		return nextTestTime;
	}
	public void setNextTestTime(String nextTestTime) {
		this.nextTestTime = nextTestTime;
	}
	public String getSensorId() {
		return sensorId;
	}
	public void setSensorId(String sensorId) {
		this.sensorId = sensorId;
	}
	public String getDetectCount() {
		return detectCount;
	}
	public void setDetectCount(String detectCount) {
		this.detectCount = detectCount;
	}
	public String getDetectCountHour() {
		return detectCountHour;
	}
	public void setDetectCountHour(String detectCountHour) {
		this.detectCountHour = detectCountHour;
	}
	public String getDetectTime() {
		return detectTime;
	}
	public void setDetectTime(String detectTime) {
		this.detectTime = detectTime;
	}
	
	
	
}
